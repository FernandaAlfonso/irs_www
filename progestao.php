        <strong>O QUE É</strong><br />
        O curso Progestão é uma iniciativa do Consed - Conselho Nacional de Secretários
        de Educação para formação em gestão administrativa e pedagógica de gestores de escolas
        públicas.<br />
        É um curso a distância que utiliza matérias impressos e tem como objetivo formar
        lideranças escolares comprometidas com a construção de um projeto de gestão democrática
        da escola pública, focada no sucesso dos alunos. Visando ampliar as oportunidades
        de acesso, aprendizado e interação do curso, o Instituto Razão Social uniu-se ao
        CONSED para desenvolver o Progestão Online.
        <br />
        <br />
        <strong>COMO FUNCIONA</strong><br />
        O Progestão Online se constrói por meio de um processo democrático e participativo,
        seguindo os mesmos passos da construção do Progestão, no qual todos aprendem e contribuem.
        Foi constituído, pelo Consed, um Comitê de Trabalho com representantes das Secretarias
        de Estado das cinco regiões do país. O objetivo deste grupo é pensar o conteúdo
        online a partir dos textos originais. Para tanto, elabora-se um mapeamento dos conteúdos
        dos módulos; a seguir o Razão Social formata as telas a partir de um trabalho de
        desenho instrucional que contém os conteúdos e interações. Estas propostas são,
        por sua vez, apresentadas novamente ao Comitê para validação, com apoio dos autores
        de cada módulo. Utilizando-se de uma plataforma de ensino a distância na internet.
