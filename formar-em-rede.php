    <div class="texto_sem_logo"> <strong>O QUE É</strong><br />
      Formação continuada para profissionais das Redes Públicas de EducaçãoInfantil 
      por meio do desenvolvimento de projetos de ação nos contextos locais.<br />
      <br />
      <strong>COMO FUNCIONA</strong><br />
      Por meio da implantação de uma comunidade virtual de formadores (equipes 
      técnicas das secretarias municipais de educação), com ações presenciais e a 
      distância. O Formar em Rede gera um contexto real de uso da tecnologia para a 
      troca de experiências, circulação e produção de conhecimentos relacionados ao 
      brincar, à aproximação com a cultura escrita, entre outros. Desta forma, o 
      Programa apoia a formação continuada de coordenadores pedagógicos, diretores e 
      professores, contribuindo para o desenvolvimento de práticas de qualidade nas 
      creches e pré-escolas públicas do nosso país.<br />
      <br />
      <strong>RESULTADOS</strong><br />
      Em 2011, além dos 9 municípios que participam autonomamente do processo, teve 
      inicio uma parceria com o Santander e MEC / Programa Pró-infância. Desta forma, 
      estão sendo beneficiados mais 19 municípios baianos, com um diferencial, 
      específico nesta edição: o trabalho com profissionais na área da saúde dos 
      municípios visando um melhor atendimento às crianças nas unidades de educação 
      infantil.<br />
      <br />
      Escolas beneficiadas: 938<br />
      Professores Beneficiados: 2176 <br />
