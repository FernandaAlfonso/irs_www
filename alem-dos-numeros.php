<div class="conteudo_left">
    <strong>O QUE É</strong><br/>
    O Além dos Números nasceu a partir dos bons resultados do Programa de formação à distância na área de alfabetização estruturado pelos mesmos parceiros – o Além das Letras. O propósito do Programa Além dos Números é incentivar mudanças positivas nos indicadores de rendimento escolar em Matemática e apoiar o sucesso escolar dos alunos do Ensino Fundamental do 1º ao 5º ano.<br /><br />
    <strong>COMO FUNCIONA</strong><br/>
    O programa atinge diretamente as equipes técnicas das Secretarias Municipais de Educação e indiretamente, coordenadores e professores, incentivando uma cultura de formação permanente no município. Para sua implementação, conta com uma comunidade de aprendizagem online onde os técnicos das Secretarias são os sujeitos da formação e reeditores reflexivos das propostas em suas redes locais.<br /><br />
    <strong>RESULTADOS</strong><br/>
    O Programa está em sua segunda edição, e conta com a participação de 10 municípios onde se encontram escolas parceiras do Instituto Escola Brasil, do Santander, além de outros 2 munícipios autônomos (Ariquemes e Sertãozinho).<br />
    Beneficiados em 2011: 233 escolas, 3331 professores e 79.679 alunos.
