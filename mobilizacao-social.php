        Mobilização das famílias e da sociedade pelo direito de todos e cada um dos brasileiros
        à educação de qualidade.<br />
        <br />
        <strong>O QUE É</strong><br />
        É o chamado do Ministério da Educação (MEC) à sociedade para o trabalho de mobilização
        das famílias e comunidades pela melhoria da qualidade da educação e pela garantia
        do direito de aprender a todos os brasileiros.<br />
        <br />
        A Mobilização é pautada pelas ações previstas no Plano de Mobilização Social pela
        Educação do MEC, que oferece orientação e incentivo a lideranças sociais, gestores
        da educação pública, profissionais da educação e representantes de segmentos organizados,
        entre outros, para a realização de ações norteadas pelo diálogo com as famílias
        a respeito da importância da educação e do acompanhamento, por parte dos pais, na
        vida escolar dos filhos.<br />
        <br />
        <strong>Para saber mais envie um e-mail para: <a href="mailto:mobilizacaosocial@mec.gov.br">
            mobilizacaosocial@mec.gov.br</a></strong><br />
        <br />
        A ação específica se dá junto aos funcionários do Consórcio CCPR no município de
        Araucária, PR, localidade onde o Grupo Promon - uma das sócias do Razão Social –
        tem uma obra consorciada à Camargo Correa. Seu objetivo é disseminar a cultura de
        participação social dos funcionários, por meio da divulgação de informações sobre
        educação, oferecendo uma oportunidade de atuação social.<br />
        <br />
        <strong>COMO FUNCIONA</strong><br />
        Despertando a consciência das pessoas sobre o direito de todos os brasileiros à
        educação de qualidade, pela mobilização das famílias e da comunidade a partir de
        atividades como campanhas de conscientização e trabalho voluntário que aproxime
        escola e comunidade.<br />
        No Plano desenvolvido no Consórcio, há duas fases do processo.<br />
        A primeira fase se dá com a mobilização interna dos funcionários da obra, por meio
        da divulgação de informações e promoção de discussões sobre Educação. A segunda
        fase, realizada pelos funcionários - multiplicadores - tem como ideia central a
        articulação da comunidade a partir da criação de espaços de discussão sobre educação
        junto a pais, professores, diretores, administração municipal e lideranças locais.<br />
        O diferencial nesta ação se deu com o envolvimento, na primeira fase, de outras
        empresas e instituições que viram na mobilização social pela educação em Araucária
        uma oportunidade de atuação conjunta, tais como o Instituto Votorantim, como suporte
        e apoio, o Instituto Paulo Montenegro do IBOPE em conjunto com o Departamento de
        Matemática da Universidade Federal do Paraná, e ainda vários outros atores locais
        e empresas estão discutindo sua participação como a Gerdau, por exemplo, que também
        está presente no município somando assim, conhecimentos, competências e otimizando
        o investimento social privado.<br />
        <br />
        <strong>RESULTADOS</strong><br />
        Em 2011 o Comitê Interno de Mobilização do Consórcio continua a desenvolver ações
        junto aos funcionários da obra, mas estende sua atuação ao participar de eventos
        no município em parceria com a Secretaria Municipal de Educação e administração
        municipal, além de outros parceiros locais. Deram início a uma ação especifica junto
        ao setor empresarial, ao propor a realização de uma Semana pela Mobilização Social
        pela Educação a ser realizada com seu apoio nas empresas locais.<br />
        <br />
        O Comitê foi convidado a participar do Conselho Municipal do PAR (Plano de Ações
        Articuladas).<br />
        <br />
        O Instituto Razão Social apoia ainda essa iniciativa do MEC com a criação de uma
        comunidade online dos parceiros do Plano de Mobilização Social pela Educação. Várias
        instituições e empresas que desenvolvem ou pretendem desenvolver ações neste sentido
        podem conhecer os demais parceiros e trocar informações, ideias e conhecimentos
        na plataforma que conta com fórum, chat, biblioteca, ente outras ferramentas de
        interação.
