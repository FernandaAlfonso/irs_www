    <div class="texto_sem_logo">   
        <strong>O QUE É</strong><br />
        Uma rede de troca de conhecimento entre municípios, capaz de ampliar a eficiência e a eficácia dos processos de alfabetização em escolas públicas municipais, por meio de um investimento na Formação Continuada dos profissionais da educação. O foco é alfabetização, com ênfase na leitura e escrita nas primeiras séries do ensino fundamental.<br />
        <br />
        <strong>COMO FUNCIONA</strong><br />
        O programa atinge diretamente as equipes técnicas das Secretarias Municipais de Educação e indiretamente, coordenadores e professores. Para sua implementação, conta com um Portal e uma comunidade de aprendizagem online. O Portal é aberto e consta de várias seções com informações, conteúdos, (entrevistas, artigos e textos) cursos, blog do professor, etc. Já a comunidade de aprendizagem é um espaço reservado à consultoria online para as formadoras das Secretarias Municipais de Educação da rede ADL..<br /><br />
        <strong>RESULTADOS</strong><br />
            O Programa, iniciado em 2004, já atingiu mais de 200 municípios. Na edição 2011 o Programa beneficia 10 municípios indicados Instituto Escola Brasil / Santander, onde há escolas de seu relacionamento em função do Programa Escola Brasil.<br /><br />
        Além destes, Cajamar e Sertãozinho – SP e Ariquemes – PA, são municípios autônomos participantes desta edição.<br /><br />
        Beneficiados em 2011: 282 escolas, 3514 professores e 87 mil alunos.<br /><br />
        Por reconhecimento do trabalho até então realizado o programa foi convidado a participar da Pesquisa "Governo, Setor Privado e Sociedade Civil para o Desenvolvimento Sustentável: Há uma Sinergia Colaborativa na América Latina". Avina - NYU (Wagner School of Public Service: PUC-Minas.
